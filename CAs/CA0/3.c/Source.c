#include <stdio.h>
int main()
{//سوال سوم: دنباله تریبوناچی
	int n, a, b, c, d;
	scanf_s("%d", &n);//وارد کردن تعداد اعداد
	scanf_s("%d %d %d", &a, &b, &c);//وارد کردن 3 عدد اول
	int i = 1;
	while (i <= n)
	{
		printf("%d ", a);
		d = a + b + c;//هر عدد برابر مجموع 3 عدد قبلی خود
		//شیفت دادن اعداد
		a = b;
		b = c;
		c = d;
		i++;//شمردن تعداد اعداد
	}
}